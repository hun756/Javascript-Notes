var a;

a = Number.MAX_VALUE; // 1.7976931348623157e+308
console.log("Max Number Value : ",a);

a = Number.MAX_SAFE_INTEGER; // 9007199254740991
console.log("Max Safe Integer:",a);

a = Number.MIN_VALUE; // 5e-324
console.log("Minimum number value :",a);

a = Number.MIN_SAFE_INTEGER; // -9007199254740991
console.log("Minimum safe integer :",a);

a = Number.EPSILON; // 0.0000000000000002220446049250313
console.log("Epsilon :", a);

a = Number.POSITIVE_INFINITY; // Infinity
console.log("Positive Infinity :", a);

a = Number.NEGATIVE_INFINITY; // -Infinity
console.log("Negative Infinity :", a);

